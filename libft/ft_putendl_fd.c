#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int n;

	if (s == 0 || fd < 0)
		return ;
	n = 0;
	while (s[n] != '\0')
	{
		write(fd, &s[n], 1);
		n++;
	}
	write(fd, "\n", 1);
}
